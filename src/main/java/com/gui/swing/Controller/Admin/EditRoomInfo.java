    public void setFieldData(Room room) {
        txtRoomName.setText(room.getRoomName());
        txtIsActive.setText(room.getRoomIsActive() ? "Yes" : "No");
        txtIsActive.setEditable(false);

        txtRoomType.removeAllItems();
        txtFloor.removeAllItems();

        FloorService floorService = context.getBean(FloorService.class);
        List<Floor> floors = floorService.getAllFloor();
        int floorIndex = -1;

        for (int i = 0; i < floors.size(); i++) {
            Floor floor = floors.get(i);
            txtFloor.addItem(floor.getFloorName());
            if (floor.getFloorId() == room.getFloor().getFloorId()) {
                floorIndex = i; 
            }
        }
        if (floorIndex >= 0) {
            txtFloor.setSelectedIndex(floorIndex); 
        }

        TypeRoomService typeRoomService = context.getBean(TypeRoomService.class);
        List<com.gui.swing.Entity.Type> listType = typeRoomService.getAllType();
        int typeIndex = -1; 

        for (int i = 0; i < listType.size(); i++) {
            com.gui.swing.Entity.Type type = listType.get(i);
            txtRoomType.addItem(type.getRoomTypeValue().toString());
            if (type.getRoomTypeValue().equals(room.getType().getRoomTypeValue())) {
                typeIndex = i; 
            }
        }
        if (typeIndex >= 0) {
            txtRoomType.setSelectedIndex(typeIndex); 
        }
    }

