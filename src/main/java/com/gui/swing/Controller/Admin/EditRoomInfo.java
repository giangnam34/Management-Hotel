    private void btnSave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave1ActionPerformed
        try {
            // Lấy thông tin từ các trường nhập liệu
            String roomName = txtRoomName.getText();
            String roomType = (String) txtRoomType.getSelectedItem();
            String floorName = (String) txtFloor.getSelectedItem();

            System.out.println(EnumTypeRoom.valueOf(roomType));

            // Tìm kiếm đối tượng Type và Floor tương ứng với thông tin đã chọn
            TypeRoomService typeRoomService = context.getBean(TypeRoomService.class);
            FloorService floorService = context.getBean(FloorService.class);

          
            com.gui.swing.Entity.Type selectedType = typeRoomService.findTypeByName(EnumTypeRoom.valueOf(roomType));
            Floor selectedFloor = floorService.findFloorByName(floorName);

            // Cập nhật thông tin vào đối tượng room
            room.setRoomName(roomName);
            room.setType(selectedType);
            room.setFloor(selectedFloor);

            // Gọi phương thức lưu của RoomService
            RoomService roomService = context.getBean(RoomService.class);
            roomService.saveRoom(room);

            // Hiển thị thông báo lưu thành công và đóng form
            JOptionPane.showMessageDialog(this, "Room information saved successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();

        } catch (Exception e) {
            // Trong trường hợp có lỗi khi lưu, hiển thị thông báo lỗi
            JOptionPane.showMessageDialog(this, "Failed to save room information. " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSave1ActionPerformed

